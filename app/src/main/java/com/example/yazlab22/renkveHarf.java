package com.example.yazlab22;

public class renkveHarf {


    private char harf;
    private int renk;

    public renkveHarf(char harf, int renk) {
        this.harf = harf;
        this.renk = renk;
    }

    public char getHarf() {
        return harf;
    }

    public void setHarf(char harf) {
        this.harf = harf;
    }

    public int getRenk() {
        return renk;
    }

    public void setRenk(int renk) {
        this.renk = renk;
    }
}
